            "```js\n"
            "Meteor.methods({\n"
            "  async 'tasks.insert'(text) {\n"
            "    check(text, String);\n"
            "    if (!this.userId) {\n"
            "      throw new Meteor.Error('not-authorized');\n"
            "    }\n"
            "    return await TasksCollection.insertAsync({\n"
            "      text,\n"
            "      owner: this.userId,\n"
            "      createdAt: new Date(),\n"
            "    });\n"
            "  },\n"
            "});\n"
            "```\n\n"
            "#### Calling Methods\n\n"
            "Use `Meteor.callAsync` on the client, which returns a Promise:\n\n"
            "```js\n"
            "const taskId = await Meteor.callAsync('tasks.insert', text);\n"
            "```\n\n"
            "The legacy `Meteor.call` with a callback still works but is "
            "deprecated.  Prefer `callAsync` for consistency with the async "
            "ecosystem.\n\n"
            "#### Optimistic UI (Simulation)\n\n"
            "When a method is defined in code shared between client and "
            "server (e.g., inside `imports/`), Meteor runs it as a **stub** "
            "on the client immediately, giving instant UI feedback.  The "
            "server then executes the same method authoritatively.  If the "
            "server result differs from the stub, Minimongo patches the "
            "client state to match.\n\n"
            "Key rules for reliable optimistic UI:\n\n"
            "- The stub must perform the same database writes as the server "
            "version.  Keep the write logic identical in both paths.\n"
            "- Skip side effects (emails, HTTP calls, logging) in the stub "
            "using `if (!this.isSimulation) { ... }`.\n"
            "- Avoid reading from external APIs in the stub since they are "
            "not available on the client.\n\n"
            "#### Error Handling\n\n"
            "Throw `Meteor.Error` for user-facing errors that should reach "
            "the client:\n\n"
            "```js\n"
            "throw new Meteor.Error('not-found', 'Task does not exist.');\n"
            "```\n\n"
            "Throw a plain `Error` for unexpected failures.  Meteor "
            "sanitizes plain errors to a generic \"Internal server error\" "
            "message before sending them to the client, preventing sensitive "
            "details from leaking.\n\n"
            "Wrap method calls in try/catch on the client:\n\n"
            "```js\n"
            "try {\n"
            "  await Meteor.callAsync('tasks.remove', taskId);\n"
            "} catch (err) {\n"
            "  // err.error  -> machine-readable code\n"
            "  // err.reason -> human-readable message\n"
            "  alert(err.reason);\n"
            "}\n"
            "```\n\n"
            "#### Structuring Methods\n\n"
            "- **One action per method.**  Name methods as "
            "`<collection>.<action>` (e.g., `tasks.insert`, "
            "`tasks.setStatus`).\n"
