            "#### Basic Parameterized Publication\n\n"
            "Accept arguments to filter data on the server side and always "
            "validate parameters:\n\n"
            "```js\n"
            "Meteor.publish('tasks.byProject', function (projectId) {\n"
            "  check(projectId, String);\n"
            "  if (!this.userId) {\n"
            "    return this.ready();\n"
            "  }\n"
            "  return TasksCollection.find(\n"
            "    { projectId, members: this.userId },\n"
            "    { fields: { text: 1, status: 1, projectId: 1 } }\n"
            "  );\n"
            "});\n"
            "```\n\n"
            "Returning `this.ready()` without a cursor signals that the "
            "publication is complete but has no data.  This is preferable to "
            "throwing an error for unauthenticated users.\n\n"
            "#### Returning Multiple Cursors\n\n"
            "A publication can return an array of cursors from *different* "
            "collections:\n\n"
            "```js\n"
            "Meteor.publish('project.detail', async function (projectId) {\n"
            "  check(projectId, String);\n"
            "  const project = await ProjectsCollection.findOneAsync({ _id: projectId });\n"
            "  if (!project) return this.ready();\n"
            "  return [\n"
            "    ProjectsCollection.find({ _id: projectId }),\n"
            "    TasksCollection.find({ projectId }),\n"
            "    Meteor.users.find(\n"
            "      { _id: { $in: project.memberIds } },\n"
            "      { fields: { profile: 1, emails: 1 } }\n"
            "    ),\n"
            "  ];\n"
            "});\n"
            "```\n\n"
            "**Important:** You cannot return multiple cursors from the same "
            "collection in one array.  If you need to, use the low-level "
            "publish API instead.\n\n"
            "#### Low-Level Publish API\n\n"
            "For complex scenarios (joining data, computed fields, or "
            "aggregations), use `this.added`, `this.changed`, and "
            "`this.removed` to manually control what documents the client "
            "sees:\n\n"
            "```js\n"
            "Meteor.publish('tasks.withOwnerName', function () {\n"
            "  const handle = TasksCollection.find().observeChanges({\n"
            "    added: async (id, fields) => {\n"
            "      const owner = await Meteor.users.findOneAsync(fields.ownerId);\n"
            "      this.added('tasks', id, {\n"
            "        ...fields,\n"
            "        ownerName: owner?.profile?.name || 'Unknown',\n"
            "      });\n"
            "    },\n"
            "    changed: (id, fields) => {\n"
            "      this.changed('tasks', id, fields);\n"
            "    },\n"
            "    removed: (id) => {\n"
            "      this.removed('tasks', id);\n"
            "    },\n"
            "  });\n\n"
            "  this.ready();\n"
            "  this.onStop(() => handle.stop());\n"
            "});\n"
            "```\n\n"
            "#### Reactive Aggregations\n\n"
            "MongoDB aggregation pipelines are not natively reactive in "
            "Meteor.  Two common approaches:\n\n"
