            "#### The Pipeline\n\n"
            "1. **Server publication** -- A function registered with "
            "`Meteor.publish` returns one or more Mongo cursors.  The server "
            "observes those cursors and sends `added`, `changed`, and "
            "`removed` DDP messages to every subscribed client.\n"
            "2. **DDP transport** -- DDP is a simple JSON protocol over "
            "WebSocket.  It handles method calls (`method`, `result`), "
            "subscription lifecycle (`sub`, `ready`, `nosub`), and data "
            "messages (`added`, `changed`, `removed`).  The client and server "
            "automatically manage connection state, reconnection, and message "
            "ordering.\n"
            "3. **Client subscription** -- On the client, `Meteor.subscribe` "
            "(or `useSubscribe` in React) opens a subscription.  The handle's "
            "`.ready()` method is reactive and returns `true` once the "
            "server's initial data set has been delivered.\n"
            "4. **Minimongo** -- The client stores published documents in "
            "Minimongo, an in-memory Mongo implementation.  You query "
            "Minimongo exactly as you would MongoDB on the server.  Because "
            "Minimongo cursors are reactive data sources, any UI bound to a "
            "cursor updates automatically when DDP messages arrive.\n"
            "5. **UI rendering** -- React components read from Minimongo "
            "inside `useTracker` hooks, which register Tracker dependencies.  "
            "When Minimongo receives a DDP change, Tracker invalidates the "
            "computation and the component re-renders with fresh data.\n\n"
            "#### DDP Message Examples\n\n"
            "```\n"
            "Server -> Client:  {\"msg\":\"added\",\"collection\":\"tasks\","
            "\"id\":\"abc\",\"fields\":{\"text\":\"Buy milk\"}}\n"
            "Server -> Client:  {\"msg\":\"changed\",\"collection\":\"tasks\","
            "\"id\":\"abc\",\"fields\":{\"done\":true}}\n"
            "Server -> Client:  {\"msg\":\"removed\",\"collection\":\"tasks\","
            "\"id\":\"abc\"}\n"
            "```\n\n"
